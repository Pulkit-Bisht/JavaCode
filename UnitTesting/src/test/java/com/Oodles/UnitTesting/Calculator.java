package com.Oodles.UnitTesting;

public class Calculator {

    //Sum Method
    public int dosum(int a, int b, int c) {
        return a + b + c;
    }

    //Multiply method

    public int doProduct(int a, int b) {
        return a * b;
    }

    //compare method

    public Boolean compareNumber(int a, int b) {
        return a == b;
    }

}
